def parse_accuracy(text):
    results = {}
    current_imp = None
    current_ds = None
    valid_datasets = ['modelnet', 'sensit', 'har', 'youtube']

    for line in text.strip().split('\n'):
        line = line.strip()
        if not line:
            continue

        # Detect imputation header (with or without colon)
        if 'IMPUTATION' in line.upper() and '|' not in line:
            current_imp = line.replace(':', '').strip().upper()
            # Normalise variations (GAUS / GAUSSIAN -> GAUSSIAN IMPUTATION)
            if 'GAUS' in current_imp:
                current_imp = 'GAUSSIAN IMPUTATION'
            elif 'ZERO' in current_imp:
                current_imp = 'ZERO IMPUTATION'
            elif 'CPM' in current_imp:
                current_imp = 'CPM IMPUTATION'
            elif 'DCP' in current_imp:
                current_imp = 'DCP IMPUTATION'

            results[current_imp] = {}
            continue

        # Detect dataset name
        if line.strip().lower() in valid_datasets:
            current_ds = line.strip().capitalize()
            if current_imp and current_ds not in results[current_imp]:
                results[current_imp][current_ds] = []
            continue

        # Extract 2‑Head accuracy values from Markdown tables
        if '|' in line and '%' in line and 'Missing Rate' not in line:
            parts = [p.strip() for p in line.split('|') if p.strip()]
            if len(parts) >= 4:
                try:
                    val_str = parts[-2].split('+/-')[0].replace('%', '').strip()
                    val = float(val_str)
                    if current_imp and current_ds:
                        results[current_imp][current_ds].append(val)
                except ValueError:
                    pass
    return results

